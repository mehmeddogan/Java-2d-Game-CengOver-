package Auxiliary;

public class Coordinate {

	private int x;
	private int y;
	
	public Coordinate(int x, int y)
	{
		this.x = x;
		this.y = y;
	}

	public int GetX() {
		return x;
	}

	public void SetX(int x) {
		this.x = x;
	}

	public int GetY() {
		return y;
	}

	public void SetY(int y) {
		this.y = y;
	}
	
	
	
}
